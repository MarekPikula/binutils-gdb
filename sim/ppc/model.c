INLINE_MODEL\
(void)
model_set(const char *name)
{
  model_enum model;
  for(model = MODEL_ppc604; model < nr_models; model++) {
    if(strcmp(name, model_name[model]) == 0) {
      current_model = model;
      return;
    }
  }

  error("Unknown model '%s', Models which are known are:%s\n",
        name,
        "\n\t604\n\t603e\n\t603\n\t601");
}

